                                <h3>REOCCURING BACK PAIN. HOW IS IT DEFINED?</h3>

                                <p>Reoccurring back pain is pain that occurs in the same location more than once. Back
                                    pain that
                                    occurs once and doesn't reoccur for another 5 years is generally not a problem.
                                    However,
                                    back pain reoccurring in the exact same location 3 or 4 times a year is a
                                    significant
                                    problem for many individuals.</p>

                                <p>A major breakthrough is the ability to assign a level to your back pain and
                                    objectively
                                    compare it with others. For any given incident of back strain, the level of pain is
                                    valuable. However, a more important indicator is amount of time it takes for the
                                    pain to
                                    disappear and for the individual to return to normal activity.</p>

                                <p>Using a Degree 1 to 4 system is simple but also valuable for research. Think of it as
                                    classifying a burn.</p>

