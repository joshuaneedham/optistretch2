                                <h3>Program for Athletes</h3>

                                <p><strong>Warming up muscles!</strong>

                                <p>The value of warming up muscle is quite controversial in the literature. First, the
                                    most
                                    important protection from muscle injury is having healthy strain resistant muscles
                                    to start
                                    with. Second, adrenaline significantly protects muscles from injury. This is why
                                    muscles are
                                    seldom injured during a true emergency activity. Third, as the body ages, the more
                                    important
                                    a good warm-up routine is. Therefore, learning routines while young is of
                                    significant value.
                                    Stretching is only one of many effective ways to warm up the muscles. Many team
                                    sports have
                                    a standard routine. At home, you can first perform the team standard routine and
                                    then the
                                    Opti-stretch warm-up afterwards to see whether all the muscles groups are
                                    sufficiently
                                    warmed up. Many have different warm-ups for different activities. As mentioned
                                    previously,
                                    the Opti-stretch warm-up can be used as a "gold standard" for comparing various
                                    routines.
                                    Depending on your body type you may need to incorporate a couple more warm-ups,
                                    especially
                                    if you have any non-correctable body asymmetries.</p>

                                <p><strong>How long does a warm-up last?</strong>

                                <p>This varies on age. For adolescents and adults under age 45 an "involuntary stretch"
                                    is
                                    adequate in the morning and lasts throughout the day until 2 hours or more of
                                    inactivity
                                    (i.e. a nap or prolonged car ride). If you are getting to an age where a 2 or more
                                    hour car
                                    ride makes you a little stiff, warm-ups before strenuous physical activity should
                                    strongly
                                    be considered.</p>

                                <p><strong>Stretching exercise.</strong>

                                <p>At last count, there are over 1,500 different stretches which have been published.
                                    There are
                                    approximately 656 muscles of the human body. I have reviewed over 150 different
                                    stretching
                                    programs (i.e. Yoga, Pilates, etc.). The stretches used by Opti-stretch (especially
                                    for the
                                    low back) have been carefully chosen for optimizing the warm-up of muscles required
                                    to lift
                                    heavy objects.</p>

                                <p><strong>Overstressing the muscles?</strong>

                                <p>Athletes can overstress muscles if their exercise routine is too demanding. This can
                                    lead to
                                    minor injury which can stall progress. You can periodically assess new routines by
                                    using a
                                    before-and-after comparison of either the Opti-stretch screening program or the
                                    Opti-stretch
                                    warm-up program. Significantly decreased ROM after an exercise routine may suggest
                                    too
                                    intense of routine. A more rapid improvement may be achieved by a more gradual
                                    increase in
                                    your routine intensity. Cooling down the muscles is highly recommended by many
                                    athletic
                                    trainers. The Opti-stretch warm-up routine is both valuable and can provide
                                    additional
                                    information regarding the work-out.</p>

                                <p><strong>Assessing for muscle injury?</strong>

                                <p>First sensation-of-stretch is a very valuable tool. If the 1st sensation has
                                    decreased, the
                                    chance of a significant injury has occurred. By pushing a strained muscle, the
                                    chance of a
                                    more serious strain occurring is increased dramatically. Everyone knows that if you
                                    move a
                                    muscle and you have pain, most likely an injury has occurred. What Opti-stretch has
                                    discovered is that even subtle shortening of the stretch-to-pain transition also
                                    indicates
                                    some type of injury has occurred.</p>

                                <p><strong>Return to 100% activity following injury. When?</strong>

                                <p>The 1st sensation of stretch has returned to normal and Opti-stretch warm-up shows
                                    symmetrical 2 inch increases in both sides of the body before engaging in 100%
                                    activity.</p>

                                <p><strong>Deep muscle massage! How to give yourself one!</strong>

                                <p>Deep muscle massage is a commonly employed by many professional athletes. This can be
                                    rather
                                    expensive for the average individual. By slowly and gently performing the
                                    Opti-stretch
                                    warm-up, you will achieve the same level of muscle relaxation and benefits for a
                                    fraction of
                                    the cost. Many find this a great stress reducer prior to bed.</p>

                                <p><strong>Determining proper activity level!</strong>

                                <p>Peak age of muscular performance is considered to be around ages 20-25. Your own
                                    individual
                                    optimal range-of-motion for your body type is generally at this age also. Many
                                    athletic
                                    individuals will continue this range-of-motion (ROM) into their 70's. Remember,
                                    optimum
                                    stretched ROM is different for different hyperflexibility body types.</p>

                                <p><strong>Fountain of Youth</strong>

                                <p>While there are many hallmarks of aging, a major indicator is range-of-motion. It
                                    always
                                    tends to decrease with advanced age. Above-average active individuals generally
                                    preserve an
                                    increased range-of-motion well into their 80's and 90's. "Spry" individuals at these
                                    ages
                                    tend to be more active and healthy. Studies in the literature suggest that increased
                                    range-of-motion at advanced ages decreases the incidence of falling and appears to
                                    be a good
                                    indicator of above average health.</p>
