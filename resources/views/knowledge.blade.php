                                <h3>Program for Children - "Dr. Mom"</h3>

                                <p><strong>Basic Concepts why the Opti-stretch screening is a valuable tool for parents
                                        who are
                                        concerned about back pain in their children.</strong></p>

                                <p><strong>Flexibility</strong></p>

                                <p>The human body is most flexible at birth. Flexibility decreases during the first year
                                    of
                                    life. Virtually all healthy 3 to 5 year olds have the same flexibility. This level
                                    of
                                    flexibility is also very common in adults who have no history of back pain (and no
                                    joint
                                    hyperflexibility). For this reason the level of flexibility at ages 3 to 5 has been
                                    determined the "gold standard of range-of-motion".</p>

                                <p>By age 6, hyperflexibility subtypes start to appear, as-well-as limitations of
                                    range-of-motion.</p>

                                <p>While many individuals with healthy backs and no history of back pain have
                                    significantly less
                                    range-of-motion than the "gold standard", Opti-stretch has determined that there is
                                    a
                                    minimum range-of-motion that needs to exist in order for an individual to both lift
                                    heavy
                                    objects and maintain a healthy back throughout life. (Details in the book
                                    "Opti-stretch
                                    Versus Recurrent Back Pain")</p>


                                <p><strong>Injury</strong></p>

                                <p>Most healthy children will have some type of bumps and bruises as they develop. The
                                    screening
                                    program is valuable in determining when the child or adolescent is ready for full
                                    activity.</p>

                                <p>Frequently we have encountered significant asymmetry in children who have suffered a
                                    fairly
                                    serious injury to that limb in the past. Knowledge of the range-of-motion prior to a
                                    serious
                                    accident will allow parents to determine if the injury has completely healed or
                                    whether the
                                    child needs to be reevaluated by their pediatrician. This allows optimum healing of
                                    any
                                    significant injury.</p>


                                <p><strong>Assessing new activity regimens (i.e. martial arts)</strong></p>

                                <p>Many activities such as martial arts, dance team, and others require a certain
                                    extreme
                                    range-of-motion. While this is generally regarded as healthy, it is important for
                                    parents to
                                    periodically assess that asymmetry is not developing in their child. If asymmetry is
                                    occurring, the risk of muscle strain and other injuries increase substantially.</p>

                                <p>Many studies on range-of-motion have determined that a certain amount of increased
                                    range-of
                                    motion is normal for overused dominant limbs (i.e. base-ball pitchers, kickers,
                                    carpenters,
                                    etc.). Rarely does this exceed 1 inch of range-of-motion increase. Also the
                                    non-dominant
                                    limb has a normal "stretch-to-pain" transition.</p>
