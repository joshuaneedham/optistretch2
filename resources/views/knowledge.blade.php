                                <p><strong>Other important concepts</strong></p>

                                <p><strong>Flexibility</strong></p>

                                <p>Many studies have shown that back pain occurs in about the same frequency for
                                    individuals who
                                    are extremely flexible and those who are not very flexible. Hyperflexible
                                    individuals with
                                    back pain exhibit similar asymmetry and (ACIMS) as other individuals with back
                                    pain.</p>

                                <p>Opti-stretch research has shown that expected relative flexibility can be estimated
                                    by
                                    assessing which joints are double-jointed (a.k.a. joint hyperflexibility or
                                    extensibility).
                                    Four groups exist: no doubled jointedness, 1st degree (thumb or finger), 2nd degree
                                    (elbow),
                                    3rd degree (knee or hip) and 4th degree (spine). With increasing joint size
                                    double-jointedness the expected relative flexibility increases.</p>

                                <p>This is very valuable information when assessing and comparing individuals.</p>

                                <p>Seldom are these different levels of hyperflexibility see before 6 years of age.</p>


                                <h3>Common Locations</h3>

                                <p>These are the most common reoccurring muscle strain locations in 4000 individuals and
                                    where
                                    Opti-stretch has been demonstrated to be extremely helpful. A complete and detailed
                                    description is beyond the scope of this website. Only a brief introduction for
                                    reference
                                    purposes is presented. (A more detailed description is found in "Opti-stretch versus
                                    Recurrent Back Pain". There is a great deal of additional information which has not
                                    yet been
                                    published.)</p>


                                <p><strong>Low Back</strong></p>

                                <p>Button 1 shows the location of the most frequent and most incapacitating area of low
                                    back
                                    pain known. Remember, this location is after several weeks of healing. Acutely, the
                                    severe
                                    pain involves the entire low back area. There are 5 different types of this problem.
                                    Briefly, the most severe worst-of-the-worst strain problem known to man is
                                    right-sided
                                    ipsilateral (same side) asymptomatic chronically injured muscles (ACIMS). A less
                                    severe
                                    problem is left-sided ipsilateral ACIMS. Two other types are seen where
                                    contralateral
                                    (opposite side) ACIMS form. This constitutes a less severe form of the problem. A
                                    5th type
                                    is reserved for the few individuals not fitting into the above mentioned types. The
                                    primary
                                    muscle strain involves the iliopsoas complex.</p>

                                <p>Button 3 is also a very common location for muscle strain of the erector spinae
                                    muscles. This
                                    is frequently seen in Level 1 form in blue collar labors experiencing a particularly
                                    demanding day working in a bend over position which they are not accustomed. Level 2
                                    and 3
                                    is commonly seen in "weekend warriors" and others with poorly conditioned erector
                                    spinae
                                    muscles.</p>

                                <p><strong>Hip</strong></p>

                                <p>Button 6 is a location frequently seen in females. It can be a serious problem and is
                                    analogous to the button 1 area in males. In its most acute form it can be mistaken
                                    for a hip
                                    bursitis.</p>

                                <p><strong>Upper low back</strong></p>

                                <p>Button 4 location is not extremely common. Frequently involves the quadratus lumborum
                                    muscle
                                    and\or multifidus musculature.</p>

                                <p><strong>Upper back</strong></p>

                                <p>Button 5 is the most common upper back problem area. It primarily involves the
                                    rhomboids
                                    which are difficult to stretch. Isometric exercises are important in returning the
                                    muscles
                                    to a healthy state. If low back ACIMS exist, these must be corrected first before
                                    any long
                                    term correction can be expected.</p>

                                <p><strong>Neck and upper shoulders</strong></p>

                                <p>Button 6 is the most common location of the shoulder and neck area. The levator
                                    scapulae and
                                    trapezius muscles are primarily involved. Two new stretches have been included in
                                    the
                                    Opti-stretch Upper Back Program for this area. Assessment and correction of low back
                                    ACIMS
                                    is again important.</p>

                                <p>Button 7 generally involves the rotational muscle of the neck. Again, for long term
                                    results,
                                    low back and upper back ACIMS must be corrected if present to achieve any long
                                    lasting
                                    results.</p>
