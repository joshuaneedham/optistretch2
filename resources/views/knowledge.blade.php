                                <p>4th Degree: Is of particular interest.</p>

                                <p>For the first 5 days the pain is so severe and unrelenting that if you have ever
                                    experienced
                                    it, you will remember it. The severity of pain is very similar to having a broken
                                    bone.
                                    Classic descriptions are that "it takes an extra 10 minutes just to put on my
                                    shoes". "Once
                                    I find a semi-comfortable position, I have to think twice whether it is even worth
                                    getting
                                    up to go to the bathroom." Only 1 in 100 individuals to 1 in 300 will experience
                                    this type
                                    of strain. It only occurs in the low back. The pain encompasses the entire low back
                                    at
                                    first. After 2-4 weeks of healing, the pain then gradually localizes over the left
                                    or right
                                    posterior superior iliac spine (boney prominence 3 fingerbreadths from the middle of
                                    the
                                    spine at the level of the belt-line [see home page figure 1]). Many report that a
