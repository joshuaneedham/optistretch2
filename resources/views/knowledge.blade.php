                                <p><strong>How to remedy the problem?</strong></p>

                                <p>Stretching, if properly performed, has proven to be an extremely valuable tool for
                                    assessing
                                    reoccurring muscle strain problems. Many complex anatomic and physiologic properties
                                    of
                                    muscles have simplified by Opti-stretch to offer a effective program.</p>

                                <p><strong>Detecting the problem</strong></p>

                                <p>It is important to understand certain basic concepts of safe and effective stretching
                                    techniques prior to attempting the Opti-stretch program.</p>

                                <p>A major goal is to utilize stretching to determine whether muscles are healthy or
                                    whether
                                    some type of chronic injury is present. Traditional medicine teaches that following
                                    strain,
                                    muscles will heal to 100%. This does apply to peripheral muscles. However, in the
                                    core
                                    muscles where both stronger and weaker muscle groups performsimilar functions, the
                                    weaker
                                    muscles in certain body types can remain in a state of chronic injury for decades.
                                    These
                                    muscle groups are extremely susceptible to reinjury thus causing reoccurring strain
                                    problems. If present, they are essentially asymptomatic (unless directly assessed).
                                    The term
                                    "asymptomatic chronically injured muscles" (ACIMS) has been coined.</p>

                                <p>Not only the primarily injured muscles display the properties of ACIMS but so do
                                    other more
                                    remote muscle groups which have presumably gone into a partial spasm state to
                                    preserve the
                                    essential functional capacity of the body. These secondary ACIMS must be corrected
                                    before
                                    primary ACIMS can be completely returned to health.</p>

                                <p><strong>Properties of ACIMS</strong></p>

                                <p>There are four classic properties which ACIMS display referred to as "the 4-T's".</p>
