                                <p>This genetically programmed activity of the human body is seldom addressed. The
                                    activity is very similar to yawning. Both yawning and involuntary stretching are
                                    poorly understood in science as their purpose in every non-aquatic mammal on
                                    earth.</p>

                                <p>In yawning the mouth and jaw go through a characteristic extreme range-of-motion
                                    (ROM). Involuntary stretching the low back moves forward, the shoulders back. Arm
                                    movement is quit variable for individuals. Usually either both arms are extended
                                    above the head and slightly backwards, or one arm extends upward while the other
                                    down and backwards.</p>

                                <p>This rather extreme ROM is comfortable and even refreshing if performed
                                    involuntarily. However, to try to achieve this precise ROM is quite uncomfortable.
                                    Presumably this is due to not only active contraction of specific muscles but active
                                    relaxation of others during the involuntary stretch.</p>

                                <p><strong>Onset in-utero</strong></p>

                                <p>Onset of involuntary stretching begins in-utero. By birth this phenomenon is
                                    remarkably similar for every healthy infant.</p>

                                <p><strong>Aquatic mammals</strong></p>

                                <p>Aquatic mammals do not display activity. Presumably it is because marine mammals
                                    never stop moving for long periods throughout their life. These mammals even swim in
                                    their sleep.</p>

                                <p><strong>Quiescent during injury and sickness</strong></p>

                                <p>During significant injury and sickness this activity is not observed.</p>

