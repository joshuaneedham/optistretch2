                            <div class="panel-body">
                                <p>This concept is not a new concept. It is refers to the musculature of the spine and
                                    central axis of the body. What is new is that these muscles somewhat different
                                    physiologic properties compared to the peripheral muscles of the body.</p>

                                <p>The current medical concept is that muscle strain will heal to 100% following injury.
                                    While this is true for peripheral muscles it is not true for core muscles. The
                                    reason is that in the core muscles there are multiple groups of muscles which
                                    perform the same anatomic function. More powerful muscles are able maintain
                                    essential anatomic movement while weaker musculature can be prevented from healing
                                    completely. This allows for the formation of asymptomatic chronically injured
                                    muscles (ACISMS). These chronically injured muscles when stressed easily become
                                    reinjured setting up a vicious cycle of reoccurring strains. This causes the
                                    phenomenon of recurrent strain syndromes.</p>
